class PHR {
  String name; // name of patient in PHR
  double height; // in meters
  double weight; // in kilograms
  int heartRate; // in beats-per-minute

  PHR(String name, double height, double weight, int heartRate) {
    this.name = name;
    this.height = height;
    this.weight = weight;
    this.heartRate = heartRate;
  }
}
